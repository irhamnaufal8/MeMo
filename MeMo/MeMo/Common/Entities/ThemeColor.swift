import SwiftUI

/// An enum that represents different theme colors.
enum ThemeColor: String {
    /// The blue theme color.
    case blue = "BLUE"
    /// The green theme color.
    case green = "GREEN"
    /// The orange theme color.
    case orange = "ORANGE"
    /// The pink theme color.
    case pink = "PINK"
    /// The purple theme color.
    case purple = "PURPLE"
    /// The red theme color.
    case red = "RED"
}
